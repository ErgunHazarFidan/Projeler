        urn.setU_marka(jComboBox1.getSelectedItem().toString());
        urn.setU_kategori(jComboBox2.getSelectedItem().toString());
        urn.setU_satis(Double.valueOf(jTextField6.getText()));
        urn.setU_gelis(Double.valueOf(jTextField4.getText()));
