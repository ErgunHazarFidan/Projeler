    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        if(jTextField1.getText().trim().equals("")||jTextField2.getText().trim().equals("")||
                jTextField3.getText().trim().equals("")||jTextField4.getText().trim().equals("")||
                jTextField5.getText().trim().equals("")||jTextField6.getText().trim().equals("")){
            JOptionPane.showMessageDialog(rootPane,"Boş alan bırakılamaz");
        }
        else{
            personel prsnl = new personel();
            prsnl.setP_adi(jTextField1.getText());
            prsnl.setP_soyadi(jTextField1.getText());
            prsnl.setP_telefon(jTextField3.getText());
            prsnl.setP_adresi(jTextField4.getText());
            prsnl.setP_puantaj(Integer.parseInt(jTextField5.getText()));
            prsnl.setP_maas(Integer.parseInt(jTextField6.getText()));
            prsnl.setK_id(Integer.parseInt(jTextField7.getText()));
           
           
            
           controller.PersoneIslemleri islm = new PersoneIslemleri();
           islm.personelEkle(prsnl);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

